    public void tableload(){
    ResultSet rs = new DBSearch().searchparent();
        
            try {
                    DefaultTableModel TblModel = (DefaultTableModel)parenttable.getModel();
                    TblModel.setRowCount(0);
                    while(rs.next()){
                        
                        String id = rs.getString("parentid");
                        String name = rs.getString("name");
                        String cID = rs.getNString("childnm");
                        String cNum = rs.getNString("contact");
                        String aderess = rs.getNString("address");
                        
                        String tbData[] = {id,name,cID,cNum,aderess};
                        
                        TblModel.addRow(tbData);
                    }
        } catch (Exception e) {
        }
    }
