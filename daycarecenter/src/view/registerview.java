    private void suBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suBTNActionPerformed
        // TODO add your handling code here:
        
        
         if(evt.getSource() == suBTN){
        
        String parentID = pidTF.getText();
        String Name = nTF.getText();
        String childName = childTF.getText();
        String contact = conTF.getText();
        String address =addTF.getText();
        
        if(parentID.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter ID","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        
        if(Name.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter  Name","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        if(childName.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter childname","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        if(contact.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter Contact","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        if(address.equals("")){
        
        JOptionPane.showMessageDialog(null, "Enter Address","Error",JOptionPane.ERROR_MESSAGE);
        
        
        }else{
        
            Insertcontroller.insertparent(parentID, Name, childName, contact, address);
        
        }
         }
         pidTF.setText("");
        nTF.setText("");
         childTF.setText("");
         conTF.setText("");
         addTF.setText("");
         
         
         tableload();
         
        
    }//GEN-LAST:event_suBTNActionPerformed

